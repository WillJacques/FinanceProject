window.onload = function () {
  const previous = document.getElementsByClassName("previous_page")[0];
  const next = document.getElementsByClassName("next_page")[0];

  previous.classList.add("button-black");
  previous.style.margin = 0
  previous.style.lineHeight = "38px"
  
  next.classList.add("button-black");
  next.style.margin = 0
  previous.style.lineHeight = "38px"
};
